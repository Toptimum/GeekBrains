# Задача-4: Дан список, заполненный произвольными целыми числами.

lst = [1, 2, 4, 5, 6, 2, 5, 2]
print(f"\nПредставляем исходный список элементов: {lst}")


# Получите новый список, элементами которого будут: 
# а) неповторяющиеся элементы исходного списка:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 2, 4, 5, 6]
lst2a = set(lst) # Во множествах хранятся только уникальные значения
print(f"Выводим новый список с уникальными элементами исходного списка: {lst2a}")


# б) элементы исходного списка, которые не имеют повторений:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 4, 6]
lst2b = []
for num in lst:
	if lst.count(num) == 1:
		lst2b.append(num)
print(f"Выводим новый список с неповторяющимися элементами исходного списка: {lst2b}")

