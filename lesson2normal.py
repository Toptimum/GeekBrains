# Задача-1: Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

numbers = [0, 1, 2, -5, 8, 9, -25, 25, 4]
sqrt_list = []

for num in numbers:
	# Квадратные корни из отрицательных чисел извлечь нельзя
	if num > 0:
		# Квадратный корень можно считать как возведение в степень 0.5
		sqrt_num = num ** 0.5
		# Проверяем, является ли квадратный корень целым числом
		if (sqrt_num).is_integer():
			sqrt_list.append(int(sqrt_num))

print(f"Оригинальный список целых чисел: {numbers}")
print(f"Новый список квадратных корней: {sqrt_list}")


