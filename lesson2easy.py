# Задача-2:
# Даны два произвольные списка.
# Удалите из первого списка элементы, присутствующие во втором списке.

print("\nЗАДАЧА №2 =================================================\n")

list1 = ["слово1", "слово22", "слово111", "слово2222", "слово11111"]
list2 = ["слово2", "слово22", "слово222", "слово2222", "слово22222", "слово222222"]

print(f"Оригинальные списки:\n{list1}\n{list2}\n")

for word1 in list1:
	if word1 in list2:
		list1.remove(word1)
		print(f"Удален элемент {word1}.")

print(f"\nСписки, после удаления повторяющихся элементов:\n{list1}\n{list2}")


