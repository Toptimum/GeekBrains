# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка, выровненного по правой стороне.
# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз
# Подсказка: воспользоваться методом .format()

print("ЗАДАЧА №1 ===================================================\n")

fruits = ["яблоко", "банан", "киви", "арбуз", "груша"]

i = 1
for fruit in fruits:
	print(str(i) + ".{:>7}".format(fruit))
	i += 1


