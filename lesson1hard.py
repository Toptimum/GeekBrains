# Пример: Вася Пупкин, 41 год, вес 49 - следует обратится к врачу!

person = {'name': '', 'surname': '', 'age': 0, 'weight': 0}

person['name'] = input("Введите ваше имя: ")
person['surname'] = input("Введите вашу фамилию: ")
person['age'] = int(input("Сколько вам лет? "))
person['weight'] = int(input("Введите ваш вес: "))

if person['age'] <= 30 and person['weight'] > 50 and person['weight'] < 120:
	print("У вас хорошее состояние!")
elif person['age'] > 30 and (person['weight'] < 50 or person['weight'] > 120):
	print("Требуется начать вести правильный образ жизни.")
else:
	print("Требуется врачебный осмотр.")


