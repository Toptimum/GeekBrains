ideal_kg = height - 100
print("Для вашего роста хорошим весом считается: ", ideal_kg, "кг")

if ideal_kg - weight < 0:
	print("У вас проблемы с весом. Рекомендуем сбросить:", ideal_kg - weight, "кг")
elif ideal_kg - weight > 0:
	print("У вас недостаточный вес. Рекомендуем набрать:", ideal_kg - weight, "кг")
else:
	print("У вас хороший вес - так держать!")
